// basic
function salam() {
  console.log("hello world!");
}

salam();

// expression
let saalam = function () {
  console.log("hello world!");
};

saalam();

// arrow
var ssalam = () => {
  console.log("hello world!");
};

var ssalam2 = () => {
  console.log("hello world!");
};

ssalam();
ssalam2();
